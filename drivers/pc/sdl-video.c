static void gp2x_text(unsigned char *screen, int x, int y, char *text, int color, int flip)
{
	int i,l,slen;
        slen=strlen(text);
	
	screen=screen+x+y*320;
	
	for (i=0;i<slen;i++) 
        {
		for (l=0;l<8;l++) 
                {
		  
			screen[l*320+0]=(fontdata8x8[((text[i])*8)+l]&0x80)?color:screen[l*320+0];
			screen[l*320+1]=(fontdata8x8[((text[i])*8)+l]&0x40)?color:screen[l*320+1];
			screen[l*320+2]=(fontdata8x8[((text[i])*8)+l]&0x20)?color:screen[l*320+2];
			screen[l*320+3]=(fontdata8x8[((text[i])*8)+l]&0x10)?color:screen[l*320+3];
			screen[l*320+4]=(fontdata8x8[((text[i])*8)+l]&0x08)?color:screen[l*320+4];
			screen[l*320+5]=(fontdata8x8[((text[i])*8)+l]&0x04)?color:screen[l*320+5];
			screen[l*320+6]=(fontdata8x8[((text[i])*8)+l]&0x02)?color:screen[l*320+6];
			screen[l*320+7]=(fontdata8x8[((text[i])*8)+l]&0x01)?color:screen[l*320+7];

		}
		screen+=8;
	} 
}




void CleanSurface(void)
{
#ifdef GP2X
  int c=4; 
  while (c--)
{
    memset (gp2x_screen8, 0x80, screenSizeInBytes);    
    gp2x_video_flip();
  }

#else
 uint32 x;

 x=screen->pitch*screen->h;

 if(SDL_MUSTLOCK(screen))
  SDL_LockSurface(screen);

 memset((uint8*)screen->pixels, 0x3F, x);

 if(SDL_MUSTLOCK(screen))
  SDL_UnlockSurface(screen);

 SDL_UpdateRect(screen, 0, 0, 0, 0);
#endif
}



void KillVideo(void)
{
#ifdef GP2X
  if (fps_str) 
    free(fps_str);
  inited&=~1;

  
#else
 if(inited&1)
 {
  SDL_QuitSubSystem(SDL_INIT_VIDEO);
 }
 inited=0;
#endif
}

int InitVideo(void)
{
#ifdef GP2X


  fps_str=malloc(3);
  fps_str[0]='5'; fps_str[1]='0'; fps_str[2]=0;
  
  CleanSurface();


  inited|=1;

  puts("Initialized GP2X VIDEO via minimal");

  srendline=0;
  erendline=239;
  return 1;


#else
 const SDL_VideoInfo *vinf;
 int flags=0;

 #ifdef BROKEN
 if(_fullscreen && _fshack)
  setenv("SDL_VIDEODRIVER",_fshack,1);
 else
 {
  if(!_fshacksave)
   unsetenv("SDL_VIDEODRIVER");
  else
   setenv("SDL_VIDEODRIVER",_fshacksave,1);
 }
 #endif
 if(SDL_InitSubSystem(SDL_INIT_VIDEO)==-1)
 {
  puts(SDL_GetError());
  return(0);
 }
 inited|=1;

 SDL_ShowCursor(0);
 tlines=_eline-_sline+1;

 vinf=SDL_GetVideoInfo();

 if(vinf->hw_available)
  flags|=SDL_HWSURFACE;

 if(_fullscreen)
  flags|=SDL_FULLSCREEN;
 flags|=SDL_HWPALETTE;

 if(_fullscreen)
 {
  exs=_xscalefs;
  eys=_yscalefs;
  eefx=_efxfs;
  if(_xres<NWIDTH*exs || _yres<tlines*eys)
  {
   puts("xscale and/or yscale out of bounds.");
   KillVideo();
   return(0);
  }
  screen = SDL_SetVideoMode(_xres, _yres, 8, flags);
 }
 else
 {
  exs=_xscale;
  eys=_yscale;
  eefx=_efx;
  screen = SDL_SetVideoMode(NWIDTH*exs, tlines*eys, 8, flags);
 }
 if(!screen)
 {
  puts(SDL_GetError());
  KillVideo();
  return(0);
 }
 inited=1;
 CleanSurface();

 SDL_WM_SetCaption("FCE Ultra","FCE Ultra");
 paletterefresh=1;
 printf("srendline %d, erendline %d\n", srendline, erendline);
 return 1;
#endif
}

